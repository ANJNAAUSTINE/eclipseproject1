package pkg;

public class Datatypes {

	public static void main(Stringoperations[] args) {

		byte a=23;
		System.out.println("byte value of a="+a);
		
		short sh=344;
		System.out.println("short value of sh="+sh);
		
		int i=44;
		System.out.println("short value of i="+i);
		
		long lng=6744l;
		System.out.println("short value of lng="+lng);
		
		float fl=567.768f;
		System.out.println("short value of fl="+fl);
		
		double db=865;
		System.out.println("short value of db="+db);
		
		boolean bl=30>40;
		System.out.println("short value of bl="+bl);
		
		char ch='t';
		System.out.println("short value of ch="+ch);
	}

}
