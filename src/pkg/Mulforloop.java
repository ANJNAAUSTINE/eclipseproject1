package pkg;

public class Mulforloop {

	public static void main(Stringoperations[] args) {

		
		//print multiplication table
		
		int j=8;
		for(int i=1;i<=10;i++)
		{
			System.out.println(i+"*"+j+"="+i*j);

		}
		
		//alphabets from a to z
		
		for(char k='a';k<='z';k++)
			
		{
			System.out.println(k);

		}
	}

}
